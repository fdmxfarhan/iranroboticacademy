$(document).ready(function(){
    
    $('#tutorials-tab').hide();
    $('#setting-tab').hide();
    $('#btn1').addClass("active");
    $('#btn1').click(function(){
        $('#btn1').addClass("active");
        $('#btn2').removeClass("active");
        $('#btn3').removeClass("active");
        $('#classes-tab').show();
        $('#tutorials-tab').hide();
        $('#setting-tab').hide();
    });
    $('#btn2').click(function(){
        $('#btn2').addClass("active");
        $('#btn1').removeClass("active");
        $('#btn3').removeClass("active");
        $('#classes-tab').hide();
        $('#tutorials-tab').show();
        $('#setting-tab').hide();
    });
    $('#btn3').click(function(){
        $('#btn3').addClass("active");
        $('#btn2').removeClass("active");
        $('#btn1').removeClass("active");
        $('#classes-tab').hide();
        $('#tutorials-tab').hide();
        $('#setting-tab').show();
    });
    
});